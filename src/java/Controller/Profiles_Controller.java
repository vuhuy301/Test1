    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("views/profile.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String firstName = request.getParameter("firstName");
        String lastName = request.getParameter("lastName");
        String address = request.getParameter("address");
        String phone = request.getParameter("phone");
        String birthday_raw = request.getParameter("birthday");
        Date birthday = Date.valueOf(birthday_raw);

        HttpSession session = request.getSession();
        Account a = (Account) session.getAttribute("account");

        boolean success = false;

        if (a != null && Common.checkPhone(phone) && Common.checkDate(birthday)) {
            a.setFirstName(firstName);
            a.setLastName(lastName);
            a.setAddress(address);
            a.setPhone(phone);
            a.setBirthday(birthday);

            AccountDAO ad = new AccountDAO();

            success = ad.update(a);

            session.setAttribute("account", a);
        }

        if (success) {
            SuportMessage.sendToast(session, 1, "Update Profiles Successful.");
        } else {
            SuportMessage.sendToast(session, 0, "Update Profiles Failed.");
        }

        response.sendRedirect("profiles");
    }

